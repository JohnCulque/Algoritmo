public class FiguraGeometrica {
   
    
    public double areaRetangulo(double base, double altura){
      return base*altura;
    }
    public double areaQuadrado(double lado){
      return lado*lado;
    }
    public double areaTriangulo(double base, double altura){
      return base*altura*0.5;
    }
}
